const getPriorityFromLetter = (letter) => {
    if (letter.charCodeAt(0) < 96) {
        return letter.charCodeAt(0) - 64 + 26;
    } else {
        return letter.charCodeAt(0) - 96;
    }
};

const getCharMapFromString = (str) => {
    const letters = str.split("");
    return letters.reduce((map, letter) => {
        map[letter] = true;
        return map;
    }, {});
};

const getCommon2 = (a, b) => {
    const [firstMap, secondMap] = [getCharMapFromString(a), getCharMapFromString(b)];
    let foundVal = "";
    Object.keys(firstMap).forEach((key) => {
        if (secondMap[key]) {
            foundVal = key;
        }
    });
    return getPriorityFromLetter(foundVal);
};

const getCommon3 = (a, b, c) => {
    const [firstMap, secondMap, thirdMap] = [getCharMapFromString(a), getCharMapFromString(b), getCharMapFromString(c)];
    let foundVal = "";
    Object.keys(firstMap).forEach((key) => {
        if (secondMap[key]) {
            if (thirdMap[key]) {
                foundVal = key;
            } else {
                secondMap[key] = false;
            }
        }
    });
    return getPriorityFromLetter(foundVal);
};


