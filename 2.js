const ROCK = "A";
const PAPER = "B";
const SCISSORS = "C";

const getTypeScore = (play) => {
    switch(play) {
        case ROCK:
            return 1;
        case PAPER:
            return 2;
        case SCISSORS:
            return 3;
    }
    return -999999;
}
const calcWinScoreForB = (playA, playB) => {
    const WIN = 6;
    const DRAW = 3;
    const LOSS = 0;
    switch(playB) {
        case ROCK:
            if (playA === ROCK)
                return DRAW;
            if (playA === PAPER)
                return LOSS;
            if (playA === SCISSORS)
                return WIN;
            break; 
        case PAPER:
            if (playA === ROCK)
                return WIN;
            if (playA === PAPER)
                return DRAW;
            if (playA === SCISSORS)
                return LOSS;
            break; 
        case SCISSORS:
            if (playA === ROCK)
                return LOSS;
            if (playA === PAPER)
                return WIN;
            if (playA === SCISSORS)
                return DRAW;
            break; 
    }
    // ???
    return LOSS;
};

const getShouldPlay = (playA, outcomeDesired) => {
    const LOSS = "X";
    const DRAW = "Y";
    const WIN = "Z";
    switch (playA) {
        case ROCK:
            if (outcomeDesired === LOSS)
                return SCISSORS;
            if (outcomeDesired === DRAW)
                return ROCK;
            if (outcomeDesired === WIN)
                return PAPER;
        case PAPER:
            if (outcomeDesired === LOSS)
                return ROCK;
            if (outcomeDesired === DRAW)
                return PAPER;
            if (outcomeDesired === WIN)
                return SCISSORS;
        case SCISSORS:
            if (outcomeDesired === LOSS)
                return PAPER;
            if (outcomeDesired === DRAW)
                return SCISSORS;
            if (outcomeDesired === WIN)
                return ROCK;
    }
    // F to pay respects for bug
    return "F";
};

const solution1 = (input) => {
    const rounds = input.split("\n");
    const scores = rounds.map((round) => {
        const [playA, playB] = round.split(" ");
        const mappedPlayB = (playB === "X") ? ROCK : ((playB === "Y") ? PAPER : SCISSORS);
        const typeScore = getTypeScore(mappedPlayB);
        const winScore =  calcWinScoreForB(playA, mappedPlayB);
        return typeScore + winScore;
    });
    return scores.reduce((total, curr) => total + curr, 0);
};

